package com.loserexe.pojo.minecraft;

public class Skin {
    private String id;
    private String state;
    private String url;
    private String variant;

    public String getId() {
        return this.id;
    }

    public String getState() {
        return this.state;
    }

    public String getUrl() {
        return this.url;
    }

    public String getVarient() {
        return this.variant;
    }
}
